require ((getenv('COMPOSER_VENDOR_DIR') ?: __DIR__.'/../vendor').'/autoload.php');

$kernel = new AppKernel('docker', false);
if (PHP_VERSION_ID < 70000) {
    $kernel->loadClassCache();
}
